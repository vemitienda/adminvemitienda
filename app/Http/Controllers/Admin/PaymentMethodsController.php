<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use App\Http\Requests\Admin\PaymentMethodRequest;
use App\Models\PaymentMethod;
use Illuminate\Http\Request;

class PaymentMethodsController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $filtrar = request()->get('query');

        $datos['infoData'] = PaymentMethod::paginate(9);

        $datos['nombreColumnas'] = collect([
            'MÃ©todo de pago' => 'name'
        ]);

        $datos['token'] = csrf_token();

        $data['data'] = $datos;

        return view('Admin.PaymentMethods.index', $data);
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('Admin.PaymentMethods.create');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PaymentMethodRequest $request)
    {
        $insert = request()->all();
        $user = PaymentMethod::create($insert);
        $user->save();
        return redirect()->route('paymentmethods.index');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return view('Admin.PaymentMethods.show');
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $data['paymentMethod'] = PaymentMethod::find($id);
        return view('Admin.PaymentMethods.edit', $data);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(PaymentMethodRequest $request, $id)
    {
        $PaymentMethod       = PaymentMethod::find($id);
        $PaymentMethod->name = request()->name;
        $PaymentMethod->save();

        return redirect()->route('paymentmethods.index');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $PaymentMethod = PaymentMethod::find($id);
        $PaymentMethod->delete();

        return redirect()->route('paymentmethods.index');
    }
}
